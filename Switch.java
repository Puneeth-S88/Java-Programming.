public class Switch {
    public static void main(String[] args) {
        String cha = "enid";

        switch (cha) {
            case "enid":
                System.out.println("enid is perfect");
                break;

            case "wed":
                System.out.println("wed is perfect");
                break;

            default:
                System.out.println("it is not perfect");
        }
    }
}