public class datatypes{
    public static void main(String[] args) {
        int a = 10;
        float b = 20.5f;
        char c = 'A';
        String d = "Hello";
        boolean e = true;

        System.out.println("Integer: " + a);
        System.out.println("Float: " + b);
        System.out.println("Character: " + c);
        System.out.println("String: " + d);
        System.out.println("Boolean: " + e);
    }
}
